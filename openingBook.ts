export function getOpeningMove(history: Move[]): Move | null {
  const seq = history.map(m => `${m.from.r},${m.from.c}-${m.to.r},${m.to.c}`);

  const matching = bookVariations.filter(v => {
    if (v.length <= seq.length) return false;
    for (let i = 0; i < seq.length; i++) if (v[i] !== seq[i]) return false;
    return true;
  });

  if (!matching.length) return null;

  const chosen = matching[Math.floor(Math.random() * matching.length)];
  const next = chosen[seq.length];
  const [from, to] = next.split('-');
  const [fr, fc] = from.split(',').map(Number);
  const [tr, tc] = to.split(',').map(Number);
  return { from: { r: fr, c: fc }, to: { r: tr, c: tc } };
}
